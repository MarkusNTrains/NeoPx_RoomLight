//-----------------------------------------------------------------------------
// static module variable



//*****************************************************************************
// description:
//   constructor
//*****************************************************************************
LedArea::LedArea()
{
    m_xs = 10;
    m_xe = 20;
    m_ys = 1;
    m_ye = 2;
    m_color = 0;
}


//*****************************************************************************
// description:
//   destructor
//*****************************************************************************
LedArea::~LedArea()
{
}


//*****************************************************************************
// description:
//   GetColumnStart
//*****************************************************************************
uint16_t LedArea::GetColumnStart(void)
{
    return this->m_xs;
}


//*****************************************************************************
// description:
//   GetColumnEnd
//*****************************************************************************
uint16_t LedArea::GetColumnEnd(void)
{
    return this->m_xe;
}
        

//*****************************************************************************
// description:
//   GetRowStart
//*****************************************************************************
uint16_t LedArea::GetRowStart(void)
{
    return this->m_ys;
}


//*****************************************************************************
// description:
//   GetRowEnd
//*****************************************************************************
uint16_t LedArea::GetRowEnd(void)
{
    return this->m_ye;
}


//*****************************************************************************
// description:
//   GetColor
//*****************************************************************************
uint32_t LedArea::GetColor(void)
{
    return this->m_color;    
}
        

//*****************************************************************************
// description:
//   Set
//*****************************************************************************
LedArea::Set(uint16_t xs, uint16_t xe, uint16_t ys, uint16_t ye, uint32_t color)
{
    this->m_xs = xs;
    this->m_xe = xe;
    this->m_ys = ys;
    this->m_ye = ye;
    this->m_color = color;
}
